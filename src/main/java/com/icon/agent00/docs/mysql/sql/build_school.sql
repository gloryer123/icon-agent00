use school_db;

CREATE TABLE schools (
                         id INT AUTO_INCREMENT PRIMARY KEY,
                         name VARCHAR(100) NOT NULL COMMENT '学校名称',
                         country VARCHAR(50) NOT NULL COMMENT '国家',
                         region VARCHAR(100) NOT NULL COMMENT '地区/城市',
                         category VARCHAR(50) COMMENT '学校类型(综合/理工/商管等)',
                         is_public TINYINT(1) DEFAULT 1 COMMENT '性质: 1-公立, 0-私立',
                         duration_str VARCHAR(50) COMMENT '学制文本(如: 4年, 3至4年)',
                         duration_years DECIMAL(2,1) COMMENT '核心学制年限数字(便于代码进行大于/小于比较)',
                         tuition_rmb INT COMMENT '预估学费(人民币/学年)',
                         features TEXT COMMENT '特色描述与核心优势',

    -- ================= 础维护字段 =================
                         status TINYINT(1) DEFAULT 1 COMMENT '状态: 1-正常启用, 0-下架/隐藏',
                         create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
                         update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间'
) COMMENT='学校基础信息单表';


